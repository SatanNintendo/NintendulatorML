void	Init (void)
{
	int i;
	LARGE_INTEGER freq;
	if (!QueryPerformanceFrequency(&freq))
	{
		MessageBox(hMainWnd, _T("Failed to determine performance counter frequency!"), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
		return;
	}
	CPFreq = freq.QuadPart;
	DirectDraw = NULL;
	PrimarySurface = NULL;
	SecondarySurface = NULL;
	Fullscreen = FALSE;
	RawMode = FALSE;
	Scanlines = FALSE;
	Mode_240p = FALSE;
	NewPalette = DefaultPalette;
	for (i = 0; i < 8; i++)
		GenerateRGB(i);
}

void	Destroy (void)
{
	Stop();
	if (PrimarySurface)
	{
		PrimarySurface->Release();
		PrimarySurface = NULL;
	}
	if (SecondarySurface)
	{
		SecondarySurface->Release();
		SecondarySurface = NULL;
	}
	if (DirectDraw)
	{
		DirectDraw->Release();
		DirectDraw = NULL;
	}
}

void	Start (void)
{
	HRESULT hr;
	DDSURFACEDESC2 ddsd;
	DDPIXELFORMAT ddpf;
	DDSCAPS2 ddscaps;
	LPDIRECTDRAWCLIPPER Clipper;
	if (Fullscreen)
	{
		if (FAILED(hr = DirectDraw->SetCooperativeLevel(hMainWnd, DDSCL_EXCLUSIVE | DDSCL_FULLSCREEN)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_DIRECTDRAW), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		if (FAILED(hr = DirectDraw->SetDisplayMode(640, 480, 16, 0, 0)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_DIRECTDRAW), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		memset(&ddsd, 0, sizeof(ddsd));
		ddsd.dwSize = sizeof(ddsd);
		ddsd.dwFlags = DDSD_CAPS | DDSD_BACKBUFFERCOUNT;
		ddsd.ddsCaps.dwCaps = DDSCAPS_PRIMARYSURFACE | DDSCAPS_FLIP | DDSCAPS_COMPLEX;
		ddsd.dwBackBufferCount = 1;
		if (FAILED(hr = DirectDraw->CreateSurface(&ddsd, &PrimarySurface, NULL)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		memset(&ddscaps, 0, sizeof(ddscaps));
		ddscaps.dwCaps = DDSCAPS_BACKBUFFER;
		if (FAILED(hr = PrimarySurface->GetAttachedSurface(&ddscaps, &SecondarySurface)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
	}
	else
	{
		if (FAILED(hr = DirectDraw->SetCooperativeLevel(hMainWnd, DDSCL_NORMAL)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_DIRECTDRAW), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		memset(&ddsd, 0, sizeof(ddsd));
		ddsd.dwSize = sizeof(ddsd);
		ddsd.dwFlags = DDSD_CAPS;
		ddsd.ddsCaps.dwCaps = DDSCAPS_PRIMARYSURFACE;
		if (FAILED(hr = DirectDraw->CreateSurface(&ddsd, &PrimarySurface, NULL)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		memset(&ddsd, 0, sizeof(ddsd));
		ddsd.dwSize = sizeof(ddsd);
		ddsd.dwFlags = DDSD_CAPS | DDSD_HEIGHT | DDSD_WIDTH;
		ddsd.ddsCaps.dwCaps = DDSCAPS_OFFSCREENPLAIN;
		ddsd.dwWidth = 256 * SizeMult;
		ddsd.dwHeight = 240 * SizeMult;
		if (FAILED(hr = DirectDraw->CreateSurface(&ddsd, &SecondarySurface, NULL)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		if (FAILED(hr = DirectDraw->CreateClipper(0, &Clipper, NULL)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		if (FAILED(hr = Clipper->SetHWnd(0, hMainWnd)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		if (FAILED(hr = PrimarySurface->SetClipper(Clipper)))
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
	}
	memset(&ddpf, 0, sizeof(ddpf));
	ddpf.dwSize = sizeof(ddpf);
	if (FAILED(hr = SecondarySurface->GetPixelFormat(&ddpf)))
	{
		MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_SURFACE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
		return;
	}
	Depth = ddpf.dwRGBBitCount;
	if ((Depth != 16) && (Depth != 24) && (Depth != 32))
	{
		MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_MODE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
		return;
	}
	if (Fullscreen)
	{
		DrawRect.left = 0;
		DrawRect.top = 0;
		DrawRect.right = 640;
		DrawRect.bottom = 480;
	}
	else
	{
		DrawRect.left = 0;
		DrawRect.top = 0;
		DrawRect.right = 256 * SizeMult;
		DrawRect.bottom = 240 * SizeMult;
	}
	if (Depth == 16)
	{
		RBits = GetBits(ddpf.dwRBitMask);
		GBits = GetBits(ddpf.dwGBitMask);
		BBits = GetBits(ddpf.dwBBitMask);
		ROfs = GetOffset(ddpf.dwRBitMask);
		GOfs = GetOffset(ddpf.dwGBitMask);
		BOfs = GetOffset(ddpf.dwBBitMask);
	}
	if (CurPalette != NewPalette)
		LoadPalette(NewPalette);
}

void	Stop (void)
{
	if (SecondarySurface)
	{
		SecondarySurface->Release();
		SecondarySurface = NULL;
	}
	if (PrimarySurface)
	{
		PrimarySurface->Release();
		PrimarySurface = NULL;
	}
	if (Fullscreen)
	{
		DirectDraw->RestoreDisplayMode();
		Fullscreen = FALSE;
	}
}

void	Repaint (void)
{
	HRESULT hr;
	if (SecondarySurface)
	{
		if (Fullscreen)
		{
			if (FAILED(hr = PrimarySurface->Flip(NULL, DDFLIP_WAIT)))
			{
				MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_RENDER), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
				return;
			}
		}
		else
		{
			if (FAILED(hr = PrimarySurface->Blt(&DrawRect, SecondarySurface, NULL, DDBLT_WAIT, NULL)))
			{
				MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_RENDER), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
				return;
			}
		}
	}
}

void	Try (void)
{
	HMODULE hDDraw;
	hDDraw = LoadLibrary(_T("ddraw.dll"));
	if (!hDDraw)
	{
		MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_DIRECTDRAW), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
		return;
	}
	DirectDrawCreateEx = (HRESULT (WINAPI *)(GUID *, LPVOID *, REFIID, IUnknown *))GetProcAddress(hDDraw, "DirectDrawCreateEx");
	if (!DirectDrawCreateEx)
	{
		MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_DIRECTDRAW), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
		return;
	}
	if (FAILED(DirectDrawCreateEx(NULL, (void **)&DirectDraw, IID_IDirectDraw7, NULL)))
	{
		MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_DIRECTDRAW), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
		return;
	}
}

void	GenerateRGB (int PalNum)
{
	int i;
	if ((PalNum < 0) || (PalNum > 7))
	{
		MessageBox(hMainWnd, _T("Illegal palette selected!"), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
		return;
	}
	for (i = 0; i < 64; i++)
	{
		Palette[PalNum][i][0] = (unsigned char)(RawPalette[PalNum][i][0] << 2);
		Palette[PalNum][i][1] = (unsigned char)(RawPalette[PalNum][i][1] << 2);
		Palette[PalNum][i][2] = (unsigned char)(RawPalette[PalNum][i][2] << 2);
	}
}

