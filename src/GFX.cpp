{
	LoadPalette(CurPalette);
}

void	LoadPalette (PALETTE NewPal)
{
	FILE *PalFile;
	TCHAR filename[MAX_PATH];
	int i;
	if (NewPal == PALETTE_MAX)
		NewPal = DefaultPalette;
	if (NewPal == PALETTE_NTSC)
	{
		for (i = 0; i < 64; i++)
		{
			Palette[NewPal][i][0] = (unsigned char)round(255 * NTSCPalette[i][0]);
			Palette[NewPal][i][1] = (unsigned char)round(255 * NTSCPalette[i][1]);
			Palette[NewPal][i][2] = (unsigned char)round(255 * NTSCPalette[i][2]);
		}
	}
	else	if (NewPal == PALETTE_PAL)
	{
		for (i = 0; i < 64; i++)
		{
			Palette[NewPal][i][0] = (unsigned char)round(255 * PALPalette[i][0]);
			Palette[NewPal][i][1] = (unsigned char)round(255 * PALPalette[i][1]);
			Palette[NewPal][i][2] = (unsigned char)round(255 * PALPalette[i][2]);
		}
	}
	else	if (NewPal < PALETTE_MAX)
	{
		_stprintf(filename, _T("%s\\%s.pal"), ProgPath, PaletteNames[NewPal]);
		PalFile = _tfopen(filename, _T("rb"));
		if (!PalFile)
		{
			MessageBox(hMainWnd, Lang::GetString(LANG_ERR_GFX_PALETTE), Lang::GetString(LANG_DLG_NINTENDULATOR), MB_OK | MB_ICONERROR);
			return;
		}
		fread(Palette[NewPal], 1, 192, PalFile);
		fclose(PalFile);
	}
	CurPalette = NewPal;
	for (i = 0; i < 64; i++)
	{
		Palette32[CurPalette][i] = (Palette[CurPalette][i][0] << ROfs) | (Palette[CurPalette][i][1] << GOfs) | (Palette[CurPalette][i][2] << BOfs);
		Palette16[CurPalette][i] = (unsigned short)(((Palette[CurPalette][i][0] >> (8 - RBits)) << ROfs) | ((Palette[CurPalette][i][1] >> (8 - GBits)) << GOfs) | ((Palette[CurPalette][i][2] >> (8 - BBits)) << BOfs));
	}
}

void	SavePalette (PALETTE PalNum)
{
	FILE *PalFile;
	TCHAR filename[MAX_PATH];
	if ((PalNum < 0) || (PalNum > 7))
		return;
	_stprintf(filename, _T("%s\\%s.pal"), ProgPath, PaletteNames[PalNum]);
	PalFile = _tfopen(filename, _T("wb"));
	if (!PalFile)
		return;
	fwrite(Palette[PalNum], 1, 192, PalFile);
	fclose(PalFile);
}

void	DrawScreen (void)
{
	DDSURFACEDESC2 ddsd;
	HRESULT hr;
	unsigned long *ptr32;
	unsigned short *ptr16;
	unsigned char *ptr8;
	int i, j, x, y;
	if (!SecondarySurface)
		return;
	memset(&ddsd, 0, sizeof(ddsd));
	ddsd.dwSize = sizeof(ddsd);
	if (FAILED(hr = SecondarySurface->Lock(NULL, &ddsd, DDLOCK_WAIT | DDLOCK_WRITEONLY, NULL)))
		return;
	if (Depth == 32)
	{
		ptr32 = (unsigned long *)ddsd.lpSurface;
		for (y = 0; y < 240; y++)
		{
			for (x = 0; x < 256; x++)
			{
				ptr32[x] = Palette32[CurPalette][DrawArray[y][x]];
			}
			ptr32 += ddsd.lPitch >> 2;
		}
	}
	else if (Depth == 16)
	{
		ptr16 = (unsigned short *)ddsd.lpSurface;
		for (y = 0; y < 240; y++)
		{
			for (x = 0; x < 256; x++)
			{
				ptr16[x] = Palette16[CurPalette][DrawArray[y][x]];
			}
			ptr16 += ddsd.lPitch >> 1;
		}
	}
	else if (Depth == 24)
	{
		ptr8 = (unsigned char *)ddsd.lpSurface;
		for (y = 0; y < 240; y++)
		{
			for (x = 0; x < 256; x++)
			{
				ptr8[x * 3 + 0] = Palette[CurPalette][DrawArray[y][x]][2];
				ptr8[x * 3 + 1] = Palette[CurPalette][DrawArray[y][x]][1];
				ptr8[x * 3 + 2] = Palette[CurPalette][DrawArray[y][x]][0];
			}
			ptr8 += ddsd.lPitch;
		}
	}
	SecondarySurface->Unlock(NULL);
}

